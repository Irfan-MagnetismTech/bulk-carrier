import NProgress from "nprogress";
import {useLoading} from 'vue-loading-overlay';
import { ref } from "vue";
import { useRouter } from "vue-router";
import Api from "../../apis/Api";
import useNotification from '../useNotification.js';

export default function useDispenser() {
    const router = useRouter();
    const dispensers = ref([]);
    const $loading = useLoading();
    const notification = useNotification();
    const dispenser = ref( {
        name: '',
        tank_id: '',
        tank_name: '',
        opening_reading: '',
        max_reading: '',
        status: '',
        is_cng: '',
        material: '',
        material_id: '',
    });

    const errors = ref('');
    const isLoading = ref(false);

    async function getDispensers() {
        //NProgress.start();
        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const {data, status} = await Api.get('/configuration/dispensers');
            dispensers.value = data.value;
            notification.showSuccess(status);
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
            //NProgress.done();
        }
    }

    async function storeDispenser(form) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.post('/configuration/dispensers', form);
            dispenser.value = data.value;
            notification.showSuccess(status);
            router.push({ name: "configuration.dispenser.index" });
        } catch (error) {
            const { data, status } = error.response;
            errors.value = notification.showError(status, data);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function showDispenser(dispenserId) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.get(`/configuration/dispensers/${dispenserId}`);
            dispenser.value = data.value;
            notification.showSuccess(status);
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function updateDispenser(form, dispenserId) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.put(
                `/configuration/dispensers/${dispenserId}`,
                form
            );
            dispenser.value = data.value;
            notification.showSuccess(status);
            router.push({ name: "configuration.dispenser.index" });
        } catch (error) {
            const { data, status } = error.response;
            errors.value = notification.showError(status, data);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function deleteDispenser(dispenserId) {

        if (!confirm('Are you sure you want to delete this dispenser?')) {
            return;
        }

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.delete( `/configuration/dispensers/${dispenserId}`);
            notification.showSuccess(status);
            await getDispensers();
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function searchDispenser(searchParam) {

        // const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        // isLoading.value = true;

        try {
            const { data, status } = await Api.get(`/common/search-dispenser`, {params: { searchParam: searchParam }});
            dispensers.value = data.value;
            notification.showSuccess(status);
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    return {
        dispensers,
        dispenser,
        getDispensers,
        searchDispenser,
        storeDispenser,
        showDispenser,
        updateDispenser,
        deleteDispenser,
        isLoading,
        errors,
    };
}
