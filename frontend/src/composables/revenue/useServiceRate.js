import NProgress from "nprogress";
import {useLoading} from 'vue-loading-overlay';
import { ref } from "vue";
import { useRouter } from "vue-router";
import Api from "../../apis/Api";
import useNotification from '../useNotification.js';

export default function useServiceRate() {
    const router = useRouter();
    const serviceRates = ref([]);
    const $loading = useLoading();
    const notification = useNotification();

    const serviceRate = ref( {
        service: "",
        service_id: "",
        rate: "",

    });

    const errors = ref('');
    const isLoading = ref(false);

    async function getServiceRates() {
        //NProgress.start();
        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const {data, status} = await Api.get('/revenue/service-rates');
            serviceRates.value = data;
            notification.showSuccess(status);
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
            //NProgress.done();
        }
    }

    async function storeServiceRate(form) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.post('/revenue/service-rates', form);
            serviceRate.value = data.value;
            notification.showSuccess(status);
            router.push({ name: "revenue.service-rate.index" });
        } catch (error) {
            const { data, status } = error.response;
            errors.value = notification.showError(status, data);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function showServiceRate(serviceRateId) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.get(`/revenue/service-rates/${serviceRateId}`);
            serviceRate.value = data.value;
            notification.showSuccess(status);
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function updateServiceRate(form, serviceRateId) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.put(
                `/revenue/service-rates/${serviceRateId}`,
                form
            );
            serviceRate.value = data.value;
            notification.showSuccess(status);
            router.push({ name: "revenue.service-rate.index" });
        } catch (error) {
            const { data, status } = error.response;
            errors.value = notification.showError(status, data);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function deleteServiceRate(serviceRateId) {

        if (!confirm('Are you sure you want to delete this serviceRate?')) {
            return;
        }

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.delete( `/revenue/service-rates/${serviceRateId}`);
            notification.showSuccess(status);
            await getServiceRates();
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    async function searchServiceRate(searchParam) {

        const loader = $loading.show({'can-cancel': false, 'loader': 'dots', 'color': '#0F6B61'});
        isLoading.value = true;

        try {
            const { data, status } = await Api.get(`/common/search-service-rate`, {params: { searchParam: searchParam }});
            serviceRates.value = data.value;
            notification.showSuccess(status);
        } catch (error) {
            const { data, status } = error.response;
            notification.showError(status);
        } finally {
            loader.hide();
            isLoading.value = false;
        }
    }

    

    return {
        serviceRates,
        serviceRate,
        getServiceRates,
        searchServiceRate,
        storeServiceRate,
        showServiceRate,
        updateServiceRate,
        deleteServiceRate,
        isLoading,
        errors,
    };
}
